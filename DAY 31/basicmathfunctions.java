/*Demonstarte all the basics Math functions of java*/

public class basicmathfunctions {
    public static void main(String[] args) {
        double x=27;
        double y=68;
        
     double z=x-y;

        System.out.println("maximum no is:"+Math.max(x, y));

        System.out.println("Squareroot of y is:"+Math.sqrt(y));

        System.out.println("power of x and y is:"+Math.pow(x,y));

        System.out.println("Logarithmic value of x is:"+Math.log(x));

        System.out.println("exp of y is:"+Math.exp(y));

        System.out.println("absolute of x-y is:"+Math.abs(z));

    }
}
